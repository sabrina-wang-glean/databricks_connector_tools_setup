import time

if not creds_ok:
    print("[SKIP] Token test failed — cannot run smoke test.")
else:
    access_token = token_data["access_token"]
    headers = {"Authorization": f"Bearer {access_token}"}
    warehouse_id = WAREHOUSE_ID
    if not warehouse_id:
        print("No WAREHOUSE_ID provided — discovering warehouses...")
        wh = requests.get(f"{WORKSPACE_HOST}/api/2.0/sql/warehouses", headers=headers, timeout=10)
        if wh.status_code == 200:
            warehouses = wh.json().get("warehouses", [])
            if warehouses:
                running = [w for w in warehouses if w.get("state") == "RUNNING"]
                chosen = (running or warehouses)[0]
                warehouse_id = chosen["id"]
                print(f"Using warehouse: {chosen['name']} ({warehouse_id}, {chosen.get('state')})")
            else:
                print("[SKIP] No SQL warehouses found.")
        else:
            print(f"[FAIL] Could not list warehouses: HTTP {wh.status_code}")

# COMMAND ----------

SQL = "SELECT o_orderstatus AS status, COUNT(*) AS cnt, ROUND(SUM(o_totalprice),2) AS total FROM samples.tpch.orders GROUP BY o_orderstatus ORDER BY total DESC LIMIT 5"

if not creds_ok or not warehouse_id:
    print("[SKIP]")
else:
    r = requests.post(f"{WORKSPACE_HOST}/api/2.0/sql/statements",
        headers=headers,
        json={"warehouse_id": warehouse_id, "statement": SQL, "wait_timeout": "30s", "disposition": "INLINE"},
        timeout=60)
    if r.status_code == 200 and r.json().get("status", {}).get("state") == "SUCCEEDED":
        result = r.json()
        cols = [c["name"] for c in result["manifest"]["schema"]["columns"]]
        rows = result["result"]["data_array"]
        print("[PASS] SQL Statement API test succeeded!")
        print("  " + "  |  ".join(cols))
        for row in rows:
            print("  " + "  |  ".join(str(v) for v in row))
    else:
        print(f"[FAIL] HTTP {r.status_code}: {r.text[:300]}")

