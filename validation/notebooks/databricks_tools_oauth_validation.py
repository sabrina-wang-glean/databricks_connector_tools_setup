# DBTITLE 1,Step 3 - M2M Token Test
# MAGIC %md
# MAGIC ## Step 3 — M2M Client Credentials Test
# MAGIC
# MAGIC **PASS:** App Connection exists in the Databricks account and the Client Secret is correct.
# MAGIC
# MAGIC **PASS does NOT prove** the full U2M flow works. Also verify:
# MAGIC - Redirect URL in the App Connection exactly matches the Glean callback URL
# MAGIC - `offline_access` scope is enabled on the App Connection
# MAGIC
# MAGIC **Common failures:**
# MAGIC - `invalid_client` — wrong Client ID or Secret
# MAGIC - `OAuth application ... not available in Databricks account` — the Client ID is a
# MAGIC   service principal credential, not an App Connection. Create one under
# MAGIC   Account Console → Settings → App Connections.

# COMMAND ----------

TOKEN_URL = f"{WORKSPACE_HOST}/oidc/v1/token"

if not CLIENT_ID or not CLIENT_SECRET:
    print("[SKIP] CLIENT_ID or CLIENT_SECRET not set.")
else:
    try:
        token_resp = requests.post(
            TOKEN_URL,
            data={"grant_type": "client_credentials", "scope": "all-apis"},
            auth=(CLIENT_ID, CLIENT_SECRET),
            timeout=10,
        )
        if token_resp.status_code == 200:
            token_data = token_resp.json()
            print("[PASS] Client credentials are valid.")
            print(f"  Token type : {token_data.get('token_type')}")
            print(f"  Expires in : {token_data.get('expires_in')} seconds")
        else:
            print(f"[FAIL] HTTP {token_resp.status_code}")
            try:
                err = token_resp.json()
                print(f"  error             : {err.get('error')}")
                print(f"  error_description : {err.get('error_description')}")
            except Exception:
                print(f"  Raw: {token_resp.text[:300]}")
    except Exception as e:
        token_resp = None
        print(f"[FAIL] {e}")

